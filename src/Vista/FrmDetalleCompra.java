    private void btnRegistraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistraActionPerformed
        int id = 0;
        String idCompra = null;
        String idArticulo = null;
        double cantidad = 0.0;

        id = Integer.parseInt(txtid.getText());
        idCompra = String.valueOf(txtidcompra.getText());
        idArticulo = String.valueOf(txtidarticulo.getText());
        cantidad = Double.valueOf(txtcantidad.getText());

        cnt.RegistraCompra(id, idCompra, idArticulo, cantidad);

        limpiar();
    }//GEN-LAST:event_btnRegistraActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        tblDetalleCompra.setModel(new javax.swing.table.DefaultTableModel(
                cnt.MostrarcompraDetalle(),
                new String[]{
                    "ID Compra Detalle", "ID Compra", "ID Articulo", "Cantidad"
                }
        ));
    }//GEN-LAST:event_btnMostrarActionPerformed

    private void btnEliminaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminaActionPerformed

        int fila = tblDetalleCompra.getSelectedRow();
        String idCompra = tblDetalleCompra.getValueAt(fila, 0).toString();
        cnt.EliminaCompra(fila, idCompra);
    }//GEN-LAST:event_btnEliminaActionPerformed
    public void limpiar() {
        txtid.setText("");
        txtidcompra.setText("");
        txtidarticulo.setText("");
        txtcantidad.setText("");
    }

